def build_us_kr_map() -> pl.DataFrame:
    rows = [dict(row) for row in US_KR_MAP_SEED]
    return pl.DataFrame(rows, schema=US_KR_MAP_SCHEMA).sort(["us_symbol", "effective_from"])


def select_active(frame: pl.DataFrame, on: date) -> pl.DataFrame:
    return frame.filter(
        (pl.col("effective_from") <= on)
        & (pl.col("effective_to").is_null() | (pl.col("effective_to") > on))
    )


def mapped_kr_symbols(frame: pl.DataFrame, us_symbol: str, on: date) -> list[str]:
    active = select_active(frame, on).filter(pl.col("us_symbol") == us_symbol)
    symbols: list[str] = []
    for row_symbols in active["kr_symbols"].to_list():
        for symbol in row_symbols:
            if symbol not in symbols:
                symbols.append(symbol)
    return symbols
